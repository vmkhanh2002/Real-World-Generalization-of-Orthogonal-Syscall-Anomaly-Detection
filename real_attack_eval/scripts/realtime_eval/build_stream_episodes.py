"""Build streaming episodes from window-score exports and a realtime manifest."""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import Any, Dict, List


SCRIPT_DIR = Path(__file__).resolve().parent
if str(SCRIPT_DIR) not in sys.path:
    sys.path.insert(0, str(SCRIPT_DIR))

import io_utils
from utils import canonical_path, max_consecutive_true, path_for_output, resolve_project_path  # type: ignore


def project_root_from_here() -> Path:
    return Path(__file__).resolve().parents[2]


def parse_args() -> argparse.Namespace:
    script_dir = Path(__file__).resolve().parent
    if str(script_dir) not in sys.path:
        sys.path.insert(0, str(script_dir))
    import defaults as rt_defaults  # pylint: disable=import-error

    parser = argparse.ArgumentParser(
        description=(
 "Create episode metadata with temporal ground-truth segments for real-time evaluation."
        )
    )
    parser.add_argument(
 "--window-scores-jsonl",
        default=str(rt_defaults.artifact_path("window_scores.jsonl")),
        help="JSONL generated by export_window_scores.py.",
    )
    parser.add_argument(
 "--manifest",
        default=str(rt_defaults.DEFAULT_FULL_MANIFEST),
        help="Manifest with intended_label and target_preset.",
    )
    parser.add_argument(
 "--mode",
        choices=["full_file_label", "prefix_attack_suffix"],
        default="prefix_attack_suffix",
        help="How to assign temporal labels for attack episodes.",
    )
    parser.add_argument(
 "--attack-onset-fraction",
        type=float,
        default=0.30,
        help="Start attack segment at floor(n_windows * fraction) in prefix_attack_suffix mode.",
    )
    parser.add_argument(
 "--attack-end-fraction",
        type=float,
        default=0.90,
        help="End attack segment near floor(n_windows * fraction) in prefix_attack_suffix mode.",
    )
    parser.add_argument(
 "--output-json",
        default=str(rt_defaults.artifact_path("stream_episodes.json")),
        help="Output episodes JSON path.",
    )
    parser.add_argument(
 "--family-onset-json",
        default=str(rt_defaults.DEFAULT_CANONICAL_FAMILY_ONSET),
        help=(
 "JSON path for per-family onset/end overrides. "
 "Supported formats: "
 "{'Hydra_SSH': {'attack_onset_fraction': 0.25, 'attack_end_fraction': 0.90}, ...} "
 "or {'default': {...}, 'families': {...}}."
        ),
    )
    parser.add_argument(
 "--no-family-onset-calibration",
        action="store_true",
        help="Disable the canonical per-family onset calibration defaults.",
    )
    return parser.parse_args()


def canonical_path(text: str) -> str:
    return text.replace("\\", "/").lower()


def resolve_project_path(project_root: Path, raw: str | Path) -> Path:
    path = Path(raw)
    return path if path.is_absolute() else (project_root / path)


def path_for_output(project_root: Path, path: Path) -> str:
    try:
        return str(path.resolve().relative_to(project_root.resolve()))
    except ValueError:
        return str(path.resolve())


def load_window_scores(path: Path) -> List[Dict[str, Any]]:
    return io_utils.load_jsonl(path)


def build_manifest_index(manifest: Dict[str, Any]) -> tuple[Dict[str, Dict[str, Any]], Dict[str, Dict[str, Any]]]:
    by_path: Dict[str, Dict[str, Any]] = {}
    by_sample: Dict[str, Dict[str, Any]] = {}
    for entry in manifest.get("entries", []):
        if not isinstance(entry, dict):
            continue
        for field in ("expected_detect_file", "expected_converted_file", "source_file"):
            path_value = entry.get(field)
            if isinstance(path_value, str) and path_value:
                by_path[canonical_path(path_value)] = entry
                break
        sample_id = entry.get("sample_id")
        if isinstance(sample_id, str) and sample_id:
            by_sample[sample_id] = entry
    return by_path, by_sample


def sample_id_from_file(path_text: str) -> str:
    return Path(path_text).stem


def clamp_int(value: int, lo: int, hi: int) -> int:
    return max(lo, min(hi, value))


def validate_fraction_pair(onset: float, end: float, context: str) -> None:
    if not (0.0 <= onset <= 1.0):
        raise ValueError(f"{context}: attack_onset_fraction must be in [0, 1].")
    if not (0.0 <= end <= 1.0):
        raise ValueError(f"{context}: attack_end_fraction must be in [0, 1].")
    if onset >= end:
        raise ValueError(f"{context}: attack_onset_fraction must be < attack_end_fraction.")


def load_family_overrides(path: Path | None) -> tuple[dict[str, dict[str, float]], dict[str, float] | None]:
    if path is None:
        return {}, None
    raw = io_utils.load_json(path)
    if not isinstance(raw, dict):
        raise ValueError("--family-onset-json must be a JSON object.")

    default_pair: dict[str, float] | None = None
    family_map: dict[str, dict[str, float]] = {}

    if "families" in raw or "default" in raw:
        families = raw.get("families", {})
        if families is None:
            families = {}
        if not isinstance(families, dict):
            raise ValueError("family-onset JSON field 'families' must be an object.")
        for family, cfg in families.items():
            if not isinstance(family, str) or not isinstance(cfg, dict):
                continue
            onset = float(cfg.get("attack_onset_fraction"))
            end = float(cfg.get("attack_end_fraction"))
            validate_fraction_pair(onset, end, f"family '{family}'")
            family_map[family] = {
 "attack_onset_fraction": onset,
 "attack_end_fraction": end,
            }
        default_cfg = raw.get("default")
        if isinstance(default_cfg, dict):
            onset = float(default_cfg.get("attack_onset_fraction"))
            end = float(default_cfg.get("attack_end_fraction"))
            validate_fraction_pair(onset, end, "default")
            default_pair = {
 "attack_onset_fraction": onset,
 "attack_end_fraction": end,
            }
    else:
        # Direct mapping by family name.
        for family, cfg in raw.items():
            if not isinstance(family, str) or not isinstance(cfg, dict):
                continue
            onset = float(cfg.get("attack_onset_fraction"))
            end = float(cfg.get("attack_end_fraction"))
            validate_fraction_pair(onset, end, f"family '{family}'")
            family_map[family] = {
 "attack_onset_fraction": onset,
 "attack_end_fraction": end,
            }
    return family_map, default_pair


def resolve_onset_end_for_episode(
    *,
    intended_label: str,
    attack_family: str | None,
    base_onset: float,
    base_end: float,
    family_overrides: dict[str, dict[str, float]] | None,
    default_override: dict[str, float] | None,
) -> tuple[float, float]:
    if intended_label != "malware_targeted":
        return base_onset, base_end
    if family_overrides and attack_family and attack_family in family_overrides:
        cfg = family_overrides[attack_family]
        return float(cfg["attack_onset_fraction"]), float(cfg["attack_end_fraction"])
    if default_override:
        return float(default_override["attack_onset_fraction"]), float(default_override["attack_end_fraction"])
    return base_onset, base_end


def build_segments_for_episode(
    *,
    intended_label: str,
    attack_family: str | None = None,
    n_windows: int,
    mode: str,
    attack_onset_fraction: float,
    attack_end_fraction: float,
    family_overrides: dict[str, dict[str, float]] | None = None,
    default_override: dict[str, float] | None = None,
) -> List[Dict[str, int | str]]:
    if n_windows <= 0:
        return []

    last_idx = n_windows - 1
    if intended_label != "malware_targeted":
        return [{"label": "benign", "start_window": 0, "end_window": last_idx}]

    if mode == "full_file_label":
        return [{"label": "attack", "start_window": 0, "end_window": last_idx}]

    if last_idx == 0:
        # A single-window attack episode cannot express prefix/attack/suffix structure.
        return [{"label": "attack", "start_window": 0, "end_window": 0}]

    onset_fraction, end_fraction = resolve_onset_end_for_episode(
        intended_label=intended_label,
        attack_family=attack_family,
        base_onset=float(attack_onset_fraction),
        base_end=float(attack_end_fraction),
        family_overrides=family_overrides,
        default_override=default_override,
    )

    attack_start = clamp_int(int(n_windows * onset_fraction), 1, last_idx)
    attack_end_candidate = int(n_windows * end_fraction) - 1
    attack_end = clamp_int(attack_end_candidate, attack_start, last_idx)

    segments: List[Dict[str, int | str]] = []
    if attack_start > 0:
        segments.append({"label": "benign", "start_window": 0, "end_window": attack_start - 1})
    segments.append({"label": "attack", "start_window": attack_start, "end_window": attack_end})
    if attack_end < last_idx:
        segments.append({"label": "benign", "start_window": attack_end + 1, "end_window": last_idx})
    return segments


def main() -> None:
    args = parse_args()
    if not (0.0 <= args.attack_onset_fraction <= 1.0):
        raise ValueError("--attack-onset-fraction must be in [0, 1].")
    if not (0.0 <= args.attack_end_fraction <= 1.0):
        raise ValueError("--attack-end-fraction must be in [0, 1].")
    if args.attack_onset_fraction >= args.attack_end_fraction and args.mode == "prefix_attack_suffix":
        raise ValueError("--attack-onset-fraction must be < --attack-end-fraction for prefix_attack_suffix.")

    project_root = project_root_from_here()
    window_scores_path = resolve_project_path(project_root, args.window_scores_jsonl)
    manifest_path = resolve_project_path(project_root, args.manifest)
    output_path = resolve_project_path(project_root, args.output_json)
    family_onset_path: Path | None = None
    if not args.no_family_onset_calibration and args.family_onset_json:
        family_onset_path = resolve_project_path(project_root, args.family_onset_json)
        if not family_onset_path.exists():
            family_onset_path = None

    window_rows = load_window_scores(window_scores_path)
    manifest = io_utils.load_json(manifest_path)
    by_path, by_sample = build_manifest_index(manifest)
    family_overrides, default_override = load_family_overrides(family_onset_path)

    episodes: List[Dict[str, Any]] = []
    unmatched = 0
    skipped_too_short = 0
    attack_count = 0
    benign_count = 0

    for row in window_rows:
        file_path = str(row.get("file", ""))
        n_windows = int(row.get("n_windows", 0))
        # Skip traces that produced zero windows they carry no scoring information
        # and would silently inflate the missed-detection count in episode_recall.
        if n_windows == 0:
            skipped_too_short += 1
            continue
        sample_id = sample_id_from_file(file_path)
        entry = by_path.get(canonical_path(file_path)) or by_sample.get(sample_id)
        if entry is None:
            unmatched += 1
            continue

        intended_label = str(entry.get("intended_label", ""))
        target_preset = entry.get("target_preset")
        attack_family = str(target_preset) if isinstance(target_preset, str) and target_preset else None
        used_onset, used_end = resolve_onset_end_for_episode(
            intended_label=intended_label,
            attack_family=attack_family,
            base_onset=float(args.attack_onset_fraction),
            base_end=float(args.attack_end_fraction),
            family_overrides=family_overrides,
            default_override=default_override,
        )
        segments = build_segments_for_episode(
            intended_label=intended_label,
            attack_family=attack_family,
            n_windows=n_windows,
            mode=args.mode,
            attack_onset_fraction=float(args.attack_onset_fraction),
            attack_end_fraction=float(args.attack_end_fraction),
            family_overrides=family_overrides,
            default_override=default_override,
        )

        if intended_label == "malware_targeted":
            attack_count += 1
        else:
            benign_count += 1

        episodes.append(
            {
 "episode_id": sample_id,
 "score_file": file_path,
 "source_file": str(entry.get("source_file", file_path)),
 "n_windows": n_windows,
 "window_size": int(row.get("window_size", 0)),
 "stride": int(row.get("stride", 0)),
 "intended_label": intended_label,
 "attack_family": target_preset,
 "ground_truth_segments": segments,
 "metadata": {
 "construction_mode": args.mode,
 "attack_onset_fraction": float(used_onset),
 "attack_end_fraction": float(used_end),
 "family_onset_override_used": bool(attack_family and attack_family in family_overrides),
                },
            }
        )

    payload = {
        "schema_version": 1,
        "manifest": path_for_output(project_root, manifest_path),
        "window_scores_jsonl": path_for_output(project_root, window_scores_path),
        "construction_mode": args.mode,
        "family_onset_json": path_for_output(project_root, family_onset_path) if family_onset_path else None,
        "summary": {
            "episodes_total": len(episodes),
            "attack_episodes": attack_count,
            "benign_episodes": benign_count,
            "unmatched_window_score_rows": unmatched,
            "skipped_too_short": skipped_too_short,
        },
        "episodes": episodes,
    }

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(json.dumps(payload, indent=2, ensure_ascii=False), encoding="utf-8")

    print("=== STREAM_EPISODES ===")
    print(
        json.dumps(
            {
                "output_json": path_for_output(project_root, output_path),
                **payload["summary"],
            },
            ensure_ascii=False,
        )
    )


if __name__ == "__main__":
    main()
