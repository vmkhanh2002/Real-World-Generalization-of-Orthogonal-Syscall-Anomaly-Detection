def max_consecutive_true(values: Sequence[bool]) -> int:
    best = 0
    cur = 0
    for value in values:
        if value:
            cur += 1
            if cur > best:
                best = cur
        else:
            cur = 0
    return best


def rolling_stats(
    probs: Sequence[float],
    positives: Sequence[bool],
    end_idx: int,
    window: int,
) -> tuple[float, float, int]:
    start_idx = max(0, end_idx - window + 1)
    p_slice = probs[start_idx: end_idx + 1]
    y_slice = positives[start_idx: end_idx + 1]
    if not p_slice:
        return 0.0, 0.0, 0
    mean_prob = float(sum(p_slice) / len(p_slice))
    positive_rate = float(sum(1 for x in y_slice if x) / len(y_slice))
    consecutive = max_consecutive_true(y_slice)
    return mean_prob, positive_rate, consecutive


def safe_div(numer: float, denom: float) -> float | None:
    if denom == 0:
        return None
    return float(numer / denom)


def percentile(values: Sequence[int], pct: float) -> float | None:
    if not values:
        return None
    ordered = sorted(float(v) for v in values)
    if len(ordered) == 1:
        return ordered[0]
    rank = (pct / 100.0) * (len(ordered) - 1)
    lo = int(math.floor(rank))
    hi = int(math.ceil(rank))
    if lo == hi:
        return ordered[lo]
    frac = rank - lo
    return ordered[lo] + frac * (ordered[hi] - ordered[lo])


def bool_segments(values: Sequence[bool]) -> List[Dict[str, int]]:
    segments: List[Dict[str, int]] = []
    start = None
    for idx, value in enumerate(values):
        if value and start is None:
            start = idx
        elif (not value) and start is not None:
            segments.append({"start_window": start, "end_window": idx - 1})
            start = None
    if start is not None:
        segments.append({"start_window": start, "end_window": len(values) - 1})
    return segments


def normalize_policy(raw_policy: Dict[str, Any]) -> Dict[str, Any]:
    trigger = raw_policy.get("trigger", {})
    clear = raw_policy.get("clear", {})
    out = {
 "policy_name": str(raw_policy.get("policy_name", "rolling_hysteresis_v1")),
 "warmup_windows": int(raw_policy.get("warmup_windows", 5)),
 "rolling_window": int(raw_policy.get("rolling_window", 20)),
 "phase5_tau": as_float(raw_policy.get("phase5_tau"), None),
 "trigger": {
 "rolling_mean_prob_gte": as_float(trigger.get("rolling_mean_prob_gte"), None),
 "rolling_positive_rate_gte": as_float(trigger.get("rolling_positive_rate_gte"), None),
 "rolling_consecutive_positive_gte": as_int(trigger.get("rolling_consecutive_positive_gte"), None),
        },
 "clear": {
 "rolling_mean_prob_lte": as_float(clear.get("rolling_mean_prob_lte"), None),
 "rolling_positive_rate_lte": as_float(clear.get("rolling_positive_rate_lte"), None),
        },
        # min_alert_hold_windows: alert cannot clear before this many windows have elapsed.
        # Default 1 preserves existing behavior (one-window minimum, avoids single-window flicker).
 "min_alert_hold_windows": max(1, int(raw_policy.get("min_alert_hold_windows", 1))),
    }
    if out["warmup_windows"] < 1:
        raise ValueError("policy.warmup_windows must be >= 1")
    if out["rolling_window"] < 1:
        raise ValueError("policy.rolling_window must be >= 1")
    return out


def passes_trigger(policy: Dict[str, Any], mean_prob: float, positive_rate: float, consecutive: int) -> bool:
    trigger = policy["trigger"]
    conditions: List[bool] = []
    if trigger["rolling_mean_prob_gte"] is not None:
        conditions.append(mean_prob >= trigger["rolling_mean_prob_gte"])
    if trigger["rolling_positive_rate_gte"] is not None:
        conditions.append(positive_rate >= trigger["rolling_positive_rate_gte"])
    if trigger["rolling_consecutive_positive_gte"] is not None:
        conditions.append(consecutive >= trigger["rolling_consecutive_positive_gte"])
    if not conditions:
        return False
    return all(conditions)


def passes_clear(policy: Dict[str, Any], mean_prob: float, positive_rate: float) -> bool:
    clear = policy["clear"]
    conditions: List[bool] = []
    if clear["rolling_mean_prob_lte"] is not None:
        conditions.append(mean_prob <= clear["rolling_mean_prob_lte"])
    if clear["rolling_positive_rate_lte"] is not None:
        conditions.append(positive_rate <= clear["rolling_positive_rate_lte"])
    if not conditions:
        return False
    return all(conditions)


def evaluate_policy_timeline(
    probs: Sequence[float],
    positives: Sequence[bool],
    policy: Dict[str, Any],
) -> Dict[str, Any]:
    n = len(probs)
    if n == 0:
        return {"state_by_window": [], "segments": []}

    warmup = int(policy["warmup_windows"])
    rolling_window = int(policy["rolling_window"])
    trigger = policy["trigger"]
    need_consecutive = trigger["rolling_consecutive_positive_gte"] is not None

    # Vectorised O(N) precomputation of rolling mean_prob and positive_rate.
    # The hysteresis state machine still runs sequentially (it is inherently stateful).
    probs_arr = np.array(probs, dtype=np.float64)
    pos_arr   = np.array(positives, dtype=np.float64)
    cs_prob   = np.concatenate([[0.0], np.cumsum(probs_arr)])
    cs_pos    = np.concatenate([[0.0], np.cumsum(pos_arr)])
    indices   = np.arange(n, dtype=np.int64)
    starts    = np.maximum(0, indices - rolling_window + 1)
    win_lens  = indices - starts + 1
    roll_mean_prob = (cs_prob[indices + 1] - cs_prob[starts]) / win_lens
    roll_pos_rate  = (cs_pos[indices + 1]  - cs_pos[starts])  / win_lens

    alert_state = False
    alert_hold_remaining = 0  # RT-6: windows remaining before clear is allowed
    min_hold = int(policy.get("min_alert_hold_windows", 1))
    state_by_window: List[bool] = []
    positives_list = list(positives)  # materialise for O(1) slicing in the loop

    for idx in range(n):
        mean_prob     = float(roll_mean_prob[idx])
        positive_rate = float(roll_pos_rate[idx])
        # Only compute consecutive count when the policy condition is active.
        if need_consecutive:
            start_i = int(starts[idx])
            consecutive = max_consecutive_true(positives_list[start_i: idx + 1])
        else:
            consecutive = 0

        if alert_state:
            if alert_hold_remaining > 0:
                alert_hold_remaining -= 1  # must hold alert  cannot clear yet
            elif passes_clear(policy, mean_prob, positive_rate):
                alert_state = False
                alert_hold_remaining = 0
        else:
            if idx + 1 >= warmup and passes_trigger(policy, mean_prob, positive_rate, consecutive):
                alert_state = True
                alert_hold_remaining = min_hold - 1  # already counting this window
        state_by_window.append(alert_state)

    segments = bool_segments(state_by_window)
    return {"state_by_window": state_by_window, "segments": segments}


def build_attack_mask(
    n_windows: int,
    ground_truth_segments: Iterable[Dict[str, Any]],
) -> tuple[List[bool], List[Dict[str, int]]]:
    mask = [False] * n_windows
    ranges: List[Dict[str, int]] = []

    for segment in ground_truth_segments:
        if str(segment.get("label", "")).lower() != "attack":
            continue
        start_raw = as_int(segment.get("start_window"), None)
        end_raw = as_int(segment.get("end_window"), None)
        if start_raw is None or end_raw is None:
            continue
        if n_windows <= 0:
            continue
        start = max(0, min(n_windows - 1, start_raw))
        end = max(0, min(n_windows - 1, end_raw))
        if end < start:
            continue
        ranges.append({"start_window": start, "end_window": end})
        for idx in range(start, end + 1):
            mask[idx] = True

    ranges.sort(key=lambda x: x["start_window"])
    return mask, ranges


def summarize_recovery_delay(alert_state: Sequence[bool], last_attack_end: int | None) -> int | None:
    if last_attack_end is None:
        return None
    if last_attack_end < 0 or last_attack_end >= len(alert_state):
        return None
    if last_attack_end == len(alert_state) - 1:
        return None
    if not alert_state[last_attack_end]:
        return 0
    idx = last_attack_end + 1
    while idx < len(alert_state) and alert_state[idx]:
        idx += 1
    return idx - (last_attack_end + 1)


def evaluate_episode(
    episode: Dict[str, Any],
    score_row: Dict[str, Any],
    policy: Dict[str, Any],
) -> Dict[str, Any]:
    intended_label = str(episode.get("intended_label", ""))
    probs = [float(x) for x in score_row.get("phase5_probs", [])]
    tau = policy.get("phase5_tau")
    if tau is None:
        tau = as_float(score_row.get("phase5_tau"), 0.5)
    positives = [p >= float(tau) for p in probs]
    if len(positives) != len(probs):
        positives = [False] * len(probs)

    n_windows = len(probs)
    attack_mask, attack_ranges = build_attack_mask(n_windows, episode.get("ground_truth_segments", []))
    has_attack = intended_label == "malware_targeted"
    has_attack_window_range = any(attack_mask)
    first_attack_start = attack_ranges[0]["start_window"] if attack_ranges else None
    last_attack_end = attack_ranges[-1]["end_window"] if attack_ranges else None

    timeline = evaluate_policy_timeline(probs, positives, policy)
    alert_state = timeline["state_by_window"]
    alert_segments = timeline["segments"]
    alert_indices = [idx for idx, v in enumerate(alert_state) if v]
    first_alert_window = alert_indices[0] if alert_indices else None

    alert_inside_attack_idx = [idx for idx in alert_indices if attack_mask[idx]]
    first_alert_inside_attack = alert_inside_attack_idx[0] if alert_inside_attack_idx else None
    detected = bool(alert_inside_attack_idx) if has_attack else False

    detection_delay = None
    if detected and first_attack_start is not None and first_alert_inside_attack is not None:
        detection_delay = max(0, int(first_alert_inside_attack - first_attack_start))

    alert_time_inside_attack = int(sum(1 for idx in alert_indices if attack_mask[idx]))
    if first_attack_start is None:
        alert_time_before_attack = 0
        alert_time_after_attack = 0
    else:
        alert_time_before_attack = int(sum(1 for idx in alert_indices if idx < first_attack_start))
        alert_time_after_attack = int(
            sum(
                1
                for idx in alert_indices
                if last_attack_end is not None and idx > last_attack_end
            )
        )

    alert_duration_total = int(len(alert_indices))
    alert_segment_count = int(len(alert_segments))
    alert_flicker_count = int(max(0, alert_segment_count - 1))

    had_false_alert = bool(alert_indices) if not has_attack else bool(alert_time_before_attack > 0)
    false_alert_count = alert_segment_count if not has_attack else int(
        sum(1 for seg in alert_segments if first_attack_start is not None and seg["end_window"] < first_attack_start)
    )
    false_alert_duration = alert_duration_total if not has_attack else alert_time_before_attack

    episode_result: Dict[str, Any] = {
 "episode_id": str(episode.get("episode_id", "")),
 "score_file": str(episode.get("score_file", "")),
 "attack_family": episode.get("attack_family"),
 "intended_label": intended_label,
 "n_windows": n_windows,
 "has_attack_segment": bool(has_attack),
 "has_attack_window_range": bool(has_attack_window_range),
 "attack_start_window": first_attack_start,
 "attack_end_window": last_attack_end,
 "detected": bool(detected),
 "first_alert_window": first_alert_window,
 "first_alert_inside_attack_window": first_alert_inside_attack,
 "detection_delay_windows": detection_delay,
 "detected_within_5_windows": bool(detected and detection_delay is not None and detection_delay <= 5),
 "detected_within_10_windows": bool(detected and detection_delay is not None and detection_delay <= 10),
 "detected_within_20_windows": bool(detected and detection_delay is not None and detection_delay <= 20),
 "alert_time_inside_attack": alert_time_inside_attack,
 "alert_time_before_attack": alert_time_before_attack,
 "alert_time_after_attack": alert_time_after_attack,
 "false_alert_before_attack": bool(alert_time_before_attack > 0),
 "had_false_alert": bool(had_false_alert),
 "first_false_alert_window": first_alert_window if had_false_alert else None,
 "false_alert_count": int(false_alert_count),
 "false_alert_duration_windows": int(false_alert_duration),
 "alert_duration_windows_total": alert_duration_total,
 "alert_segment_count": alert_segment_count,
 "alert_flicker_count": alert_flicker_count,
 "recovery_delay_after_attack_end": summarize_recovery_delay(alert_state, last_attack_end),
 "policy_phase5_tau": float(tau),
 "alert_segments": alert_segments,
 "window_true_alert_count": alert_time_inside_attack,
 "window_alert_count": alert_duration_total,
 "window_attack_count": int(sum(1 for x in attack_mask if x)),
    }
    return episode_result


def evaluate_episode_result(
    episode: Dict[str, Any],
    score_row: Dict[str, Any],
    policy: Dict[str, Any],
) -> rt_models.EpisodeEvaluationResult:
    payload = evaluate_episode(episode, score_row, policy)
    declared_n_windows = as_int(episode.get("n_windows"), None)
    if declared_n_windows is not None:
        payload["declared_n_windows"] = declared_n_windows
    return rt_models.EpisodeEvaluationResult.from_mapping(payload)


def coerce_episode_results(
    values: Sequence[Any],
) -> List[rt_models.EpisodeEvaluationResult]:
    rows: List[rt_models.EpisodeEvaluationResult] = []
    for item in list(values or []):
        if isinstance(item, rt_models.EpisodeEvaluationResult):
            rows.append(item)
        elif hasattr(item, "to_dict"):
            rows.append(rt_models.EpisodeEvaluationResult.from_mapping(item.to_dict()))
        else:
            rows.append(rt_models.EpisodeEvaluationResult.from_mapping(dict(item)))
    return rows


def build_attack_family_metrics(
    episode_results: Sequence[Any],
) -> Dict[str, rt_models.AttackFamilyMetrics]:
    families: Dict[str, List[rt_models.EpisodeEvaluationResult]] = {}
    for row in coerce_episode_results(episode_results):
        if not row.has_attack_segment:
            continue
        family = str(row.attack_family or "UNKNOWN")
        families.setdefault(family, []).append(row)

    out: Dict[str, rt_models.AttackFamilyMetrics] = {}
    for family, rows in sorted(families.items(), key=lambda item: item[0]):
        attack_episodes = len(rows)
        detected_rows = [row for row in rows if row.detected]
        delays = [
            int(row.detection_delay_windows)
            for row in detected_rows
            if row.detection_delay_windows is not None
        ]
        out[family] = rt_models.AttackFamilyMetrics(
            attack_episodes=attack_episodes,
            episode_recall=safe_div(len(detected_rows), attack_episodes),
            recall_within_5_windows=safe_div(
                sum(1 for row in rows if row.detected_within_5_windows),
                attack_episodes,
            ),
            recall_within_10_windows=safe_div(
                sum(1 for row in rows if row.detected_within_10_windows),
                attack_episodes,
            ),
            recall_within_20_windows=safe_div(
                sum(1 for row in rows if row.detected_within_20_windows),
                attack_episodes,
            ),
            median_detection_delay_windows=(float(median(delays)) if delays else None),
            p90_detection_delay_windows=percentile(delays, 90.0),
            false_alert_before_attack_rate=safe_div(
                sum(1 for row in rows if row.false_alert_before_attack),
                attack_episodes,
            ),
        )
    return out


def aggregate_family_metrics(episode_results: Sequence[Dict[str, Any]]) -> Dict[str, Dict[str, Any]]:
    return {
        family: metrics.to_dict()
        for family, metrics in build_attack_family_metrics(episode_results).items()
    }


def build_top_failures_report(
    episode_results: Sequence[Any],
    top_n: int,
) -> rt_models.TopFailuresReport:
    rows = coerce_episode_results(episode_results)
    missed_attacks = sorted(
        [row for row in rows if row.has_attack_segment and not row.detected],
        key=lambda row: (str(row.attack_family or ""), row.episode_id),
    )[:top_n]
    slow_detected = sorted(
        [
            row
            for row in rows
            if row.has_attack_segment and row.detection_delay_windows is not None
        ],
        key=lambda row: int(row.detection_delay_windows or 0),
        reverse=True,
    )[:top_n]
    benign_false_alert = sorted(
        [
            row
            for row in rows
            if (not row.has_attack_segment) and row.had_false_alert
        ],
        key=lambda row: row.false_alert_duration_windows,
        reverse=True,
    )[:top_n]

    def compact(
        values: Sequence[rt_models.EpisodeEvaluationResult],
    ) -> List[rt_models.TopFailureExample]:
        return [
            rt_models.TopFailureExample(
                episode_id=row.episode_id,
                attack_family=row.attack_family,
                detection_delay_windows=row.detection_delay_windows,
                first_alert_window=row.first_alert_window,
                false_alert_duration_windows=row.false_alert_duration_windows,
                score_file=row.score_file,
            )
            for row in values
        ]

    return rt_models.TopFailuresReport(
        missed_attacks=compact(missed_attacks),
        slow_detected_attacks=compact(slow_detected),
        benign_false_alert_examples=compact(benign_false_alert),
    )


def build_top_failures(episode_results: Sequence[Dict[str, Any]], top_n: int) -> Dict[str, Any]:
    return build_top_failures_report(episode_results, top_n).to_dict()


